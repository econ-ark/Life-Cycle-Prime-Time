#!/usr/bin/env bash
# benchmark_results.sh - Display benchmark results in a formatted table
#
# Usage: ./benchmark_results.sh [full|min|all]
#
# Displays benchmark results from reproduce/benchmarks/results/ as a
# formatted table showing key metrics from each successful run.

set -euo pipefail

BOLD="\033[1m"
DIM="\033[2m"
RESET="\033[0m"
GREEN="\033[32m"
RED="\033[31m"
YELLOW="\033[33m"

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
RESULTS_DIR="$SCRIPT_DIR/results"
AUTO_DIR="$RESULTS_DIR/autogenerated"
SAVED_DIR="$RESULTS_DIR/saved"

MODE="${1:-all}"

show_help() {
    cat << 'EOF'
Usage: ./benchmark_results.sh [MODE]

Display Life-Cycle-Prime-Time benchmark results.

MODES:
  full    Show full reproduction benchmarks only
  min     Show minimal reproduction benchmarks only
  all     Show all benchmarks (default)

EXAMPLES:
  ./benchmark_results.sh          # Show all
  ./benchmark_results.sh min      # Show only minimal runs
  ./benchmark_results.sh full     # Show only full runs

EOF
}

format_duration() {
    local seconds=$1
    if [[ "$seconds" -ge 3600 ]]; then
        printf "%dh%02dm" $((seconds / 3600)) $(((seconds % 3600) / 60))
    elif [[ "$seconds" -ge 60 ]]; then
        printf "%dm%02ds" $((seconds / 60)) $((seconds % 60))
    else
        printf "%ds" "$seconds"
    fi
}

format_timestamp() {
    local iso_timestamp=$1
    if date --version &>/dev/null 2>&1; then
        date -d "$iso_timestamp" "+%b %d %H:%M" 2>/dev/null || echo "$iso_timestamp"
    else
        date -j -f "%Y-%m-%dT%H:%M:%SZ" "$iso_timestamp" "+%b %d %H:%M" 2>/dev/null || echo "$iso_timestamp"
    fi
}

if [[ "$MODE" == "--help" || "$MODE" == "-h" ]]; then
    show_help
    exit 0
fi

if [[ ! "$MODE" =~ ^(full|min|all)$ ]]; then
    echo "Error: Invalid mode '$MODE'. Must be one of: full, min, all"
    echo ""
    show_help
    exit 1
fi

if [[ ! -d "$RESULTS_DIR" ]]; then
    echo "No results directory found at: $RESULTS_DIR"
    echo "Run reproduce.sh or reproduce_min.sh first to generate benchmarks."
    exit 0
fi

if ! command -v jq &> /dev/null; then
    echo "Error: jq is required but not installed."
    echo "Install with: brew install jq  (macOS) or  apt-get install jq  (Linux)"
    exit 1
fi

# Collect JSON files from both directories
json_files=()
if [[ -d "$AUTO_DIR" ]]; then
    while IFS= read -r f; do
        json_files+=("$f")
    done < <(find "$AUTO_DIR" -name "*.json" -type f 2>/dev/null | sort -r)
fi
if [[ -d "$SAVED_DIR" ]]; then
    while IFS= read -r f; do
        json_files+=("$f")
    done < <(find "$SAVED_DIR" -name "*.json" -type f 2>/dev/null | sort -r)
fi

if [[ ${#json_files[@]} -eq 0 ]]; then
    echo "No benchmark results found."
    echo "Run reproduce.sh or reproduce_min.sh to generate benchmarks."
    exit 0
fi

# Filter and validate
filtered_files=()
broken_files=0
for file in "${json_files[@]}"; do
    if ! jq empty "$file" 2>/dev/null; then
        ((broken_files++)) || true
        continue
    fi

    if [[ "$MODE" == "all" ]]; then
        filtered_files+=("$file")
    else
        file_kind=$(jq -r '.reproduction_kind // "unknown"' "$file" 2>/dev/null || echo "unknown")
        if [[ "$file_kind" == "$MODE" ]]; then
            filtered_files+=("$file")
        fi
    fi
done

if [[ ${#filtered_files[@]} -eq 0 ]]; then
    echo "No benchmark results found for mode: $MODE"
    exit 0
fi

echo ""
echo -e "${BOLD}Life-Cycle-Prime-Time Benchmark Results${RESET}"
if [[ "$MODE" != "all" ]]; then
    echo -e "${DIM}Filtering by kind: $MODE${RESET}"
fi
echo ""

printf "${BOLD}%-13s %-6s %-18s %-6s %8s %6s  %-24s${RESET}\n" \
    "DATE" "KIND" "HARDWARE" "RAM" "DURATION" "STATUS" "NOTES"
printf "%.100s\n" "$(printf '%0.s-' {1..100})"

for file in "${filtered_files[@]}"; do
    timestamp=$(jq -r '.timestamp // "unknown"' "$file" 2>/dev/null || echo "unknown")
    kind=$(jq -r '.reproduction_kind // "?"' "$file" 2>/dev/null || echo "?")
    duration=$(jq -r '.duration_seconds // 0' "$file" 2>/dev/null || echo "0")
    exit_status=$(jq -r '.exit_status // 1' "$file" 2>/dev/null || echo "1")
    notes=$(jq -r '.metadata.notes // ""' "$file" 2>/dev/null || echo "")

    cpu_model=$(jq -r '.system.cpu.model // ""' "$file" 2>/dev/null || echo "")
    ram_gb=$(jq -r '.system.memory.total_gb // ""' "$file" 2>/dev/null || echo "")

    date_str=$(format_timestamp "$timestamp")
    duration_str=$(format_duration "$duration")

    if [[ "$exit_status" == "0" ]]; then
        status="${GREEN}OK${RESET}"
    else
        status="${RED}FAIL${RESET}"
    fi

    if [[ -n "$cpu_model" && "$cpu_model" != "null" ]]; then
        hardware_str="$cpu_model"
        if [[ ${#hardware_str} -gt 16 ]]; then
            hardware_str="${hardware_str:0:16}"
        fi
    else
        hardware_str="-"
    fi

    if [[ -n "$ram_gb" && "$ram_gb" != "null" ]]; then
        ram_int=$(printf "%.0f" "$ram_gb")
        ram_str="${ram_int}GB"
    else
        ram_str="-"
    fi

    if [[ "$file" =~ /saved/ ]]; then
        notes="[SAVED] $notes"
    fi
    if [[ ${#notes} -gt 22 ]]; then
        notes="${notes:0:19}..."
    fi

    printf "%-13s %-6s %-18s %-6s %8s " \
        "$date_str" "$kind" "$hardware_str" "$ram_str" "$duration_str"
    printf "%-6b  %-24s\n" "$status" "$notes"
done

echo ""
printf "%.100s\n" "$(printf '%0.s-' {1..100})"
echo -e "${DIM}Total: ${#filtered_files[@]} benchmark(s)${RESET}"
if [[ $broken_files -gt 0 ]]; then
    echo -e "${YELLOW}Skipped $broken_files broken/malformed JSON file(s)${RESET}"
fi
echo ""
echo -e "${BOLD}View details:${RESET}  jq . $RESULTS_DIR/latest.json"
echo -e "${BOLD}Save a result:${RESET} cp $AUTO_DIR/<file>.json $SAVED_DIR/"
echo ""
